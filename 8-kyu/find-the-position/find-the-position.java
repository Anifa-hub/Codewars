public class Kata
{
  public static String position(char alphabet)
  {
    if(alphabet.equals("a")){return 1;}
    else if(alphabet.equals("b")){return 2;}
    else if(alphabet.equals("c")){return 3;}
    else if(alphabet.equals("d")){return 4;}
    else if(alphabet.equals("e")){return 5;}
    else if(alphabet.equals("f")){return 6;}
  }
}